    .set noreorder

LEAF(_spi_start)
    .org 0x000
    b       START
    #------------------------------------------------------------------------------------
    # BOOT STAGE 1
    .org 0x500
    START:
    #------------------------------------------------------------------------------------
    # # SPI DEBUG CODEs
    # li s0, 0xBFD10000
    # li s1, ~0x1
    # sw s1, 0(s0)
    # b  START
    # nop

    #------------------------------------------------------------------------------------
    # Jump to main function, start of the c/c++ code
    jal	main
    nop
            

END(_spi_start)



